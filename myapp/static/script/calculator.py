def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}


def calculator():
    print(logo)
    num1 = float(input("What's the first number? "))
    for i in operations:
        print(i)

    again = True
    while again:
        operation_symbol = input("Pick an operation: ")
        num2 = float(input("What's the next number? "))
        answer = operations[f"{operation_symbol}"](num1, num2)
        print(f"{num1} {operation_symbol} {num2} = {answer}")
        if input(f"Type 'Y' to continue calculating with {answer}, or type 'N' to start a new calculation ").upper() == "Y":
            num1 = answer
        else:
            again = False
            calculator()


calculator()
